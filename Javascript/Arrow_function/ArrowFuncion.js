// Funzione tradizionale
function somma(a, b) {
    return a + b + 100;
}

// Arrow Function
let somma = (a, b) => a + b + 100;

// Funzione tradizionale (senza parametri)
let a = 4;
let b = 2;
function somma () {
    return a + b + 100;
}

// Arrow Function (senza parametri)
let a = 4;
let b = 2;
let somma = () => a + b + 100;
